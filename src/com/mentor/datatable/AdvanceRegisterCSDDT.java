package com.mentor.datatable;

import java.util.*;

public class AdvanceRegisterCSDDT {

	private int srno;
	private String date;
	private String description;
	private double deposite_amt;
	private double permit_amt;
	private double balance;
	
	
	public int getSrno() {
		return srno;
	}
	public void setSrno(int srno) {
		this.srno = srno;
	}
	


	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public double getDeposite_amt() {
		return deposite_amt;
	}
	public void setDeposite_amt(double deposite_amt) {
		this.deposite_amt = deposite_amt;
	}
	public double getPermit_amt() {
		return permit_amt;
	}
	public void setPermit_amt(double permit_amt) {
		this.permit_amt = permit_amt;
	}
	public double getBalance() {
		return balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}
}
